    """
    Main execution block for processing centerline uncertainty overlay.
    
    This script processes a list of test samples, loading both vessel segmentation
    predictions and centerline uncertainty maps, then overlays the uncertainty
    values onto the vessel regions and saves the results.
    """
        
    # Load list of test samples to process
    file_list = open("../test-samples.csv").readlines()
    sorted(file_list)
    
    # Process each sample in the test list
    for basename_ext in file_list:
        basename = basename_ext.split(".")[0]
        
        # Find vessel segmentation file (TIFF format)
        vl_file = glob.glob(os.path.join(vl_dir, f"*{basename}*tiff"))
        assert len(vl_file) == 1, "multiple files found"
        vl_file = vl_file[0]

        # Find centerline uncertainty file (NPY format)
        cl_file = glob.glob(os.path.join(cl_dir, f"*{basename}*npy"))
        assert len(cl_file) == 1, "multiple files found"
        cl_file = cl_file[0]

        # Define output file paths
        ol_file_npy = os.path.join(save_dir, f"{basename}_uncertainty.npy")   # NumPy format
        ol_file_tiff = os.path.join(save_dir, f"{basename}_uncertainty.tiff") # TIFF format

        test_start_time = time()
        
        # Load centerline uncertainty map from .npy file (values in [0,1] range)
        cl_npy = np.load(cl_file).astype(np.float32)
        
        # Load 3D vessel segmentation from TIFF file and normalize to [0,1] range
        vl_npy = iio.imread(vl_file).astype(np.float32)
        vl_npy = vl_npy / np.max(vl_npy)

        # Ensure both arrays have the same dimensions
        assert cl_npy.shape == vl_npy.shape, "shape mismatch"
        
        # Create initial mask combining centerline and vessel regions
        init_npy = np.where(cl_npy > 0, cl_npy, vl_npy)
        
        # Overlay centerline uncertainty onto vessel regions
        ol_npy = overlay_nn_3d_withinvessel(vl_npy, cl_npy)

        # Save results in both NumPy and TIFF formats
        np.save(ol_file_npy, ol_npy)
        iio.imwrite(ol_file_tiff, ol_npy)
        
        print(f"Saved as {ol_file_npy} and {ol_file_tiff}")

        test_end_time = time()
        print(f"Processing {basename} took {(test_end_time - test_start_time)/60:.2f} minutes")