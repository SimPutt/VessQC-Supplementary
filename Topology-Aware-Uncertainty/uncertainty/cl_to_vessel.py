"""
Centerline to Vessel Uncertainty Overlay Module

This module processes float centerline uncertainty maps (generated by test.py) and overlays them onto vessel segmentation
predictions (generated by segmentation_unet3d). It uses a nearest neighbor approach within vessel regions to propagate uncertainty
values from centerline points to all vessel voxels. This is to restore the vessel thickness.

The main functionality includes:
- Loading centerline uncertainty maps and vessel segmentation predictions
- Finding nearest centerline points for each vessel voxel within a 64x64x64 cube
- Checking connectivity paths between centerline points and vessel voxels
- Overlaying uncertainty values onto vessel regions
- Saving results in both .npy and .tiff formats
"""

